# Display a message with print()
print("Hello, World!")

# Create a variable to store the name -> change to input()
name = input()
# Print the name
print("Hello, " + name + "!")

a = 1
b = 2
print(a + b)

a = input()
b = input()
print(a + b)

# Notes:
# input() -> strings    -> "1" + "2" = "12"
# int()   -> integers   -> 1 + 2 = 3
# float() -> decimals   -> 1.0 + 2.0 = 3.0